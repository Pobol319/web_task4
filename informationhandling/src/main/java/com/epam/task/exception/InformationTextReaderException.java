package com.epam.task.exception;

public class InformationTextReaderException extends Exception {
    public InformationTextReaderException() {
        super();
    }

    public InformationTextReaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public InformationTextReaderException(String message) {
        super(message);
    }

    public InformationTextReaderException(Throwable cause) {
        super(cause);
    }
}
